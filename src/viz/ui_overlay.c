static const unsigned char UI_UNKNOWN_GLYPH[UI_OVERLAY_GLYPH_H] = {
    0x0E, 0x11, 0x01, 0x02, 0x04, 0x00, 0x04};

static bool reserve_vertices(UiOverlayMesh *mesh, size_t additional_vertices) {
  float *resized;
  size_t required_vertices;
  size_t new_capacity;

  if (!mesh) return false;

  required_vertices = mesh->vertex_count + additional_vertices;
  if (required_vertices <= mesh->vertex_capacity) return true;

  new_capacity = mesh->vertex_capacity ? mesh->vertex_capacity : 128;
  while (new_capacity < required_vertices) {
    new_capacity *= 2;
  }

  resized = realloc(mesh->data,
                    new_capacity * UI_OVERLAY_VERTEX_FLOATS * sizeof(float));
  if (!resized) return false;

  mesh->data = resized;
  mesh->vertex_capacity = new_capacity;
  return true;
}

static const unsigned char *lookup_glyph(char ch) {
  if (ch == '\0') return UI_UNKNOWN_GLYPH;

  for (size_t i = 0; i < sizeof(UI_GLYPHS) / sizeof(UI_GLYPHS[0]); i++) {
    if ((unsigned char)UI_GLYPHS[i].ch == (unsigned char)ch) {
      return UI_GLYPHS[i].rows;
    }
  }
  return UI_UNKNOWN_GLYPH;
}

static bool emit_vertex(UiOverlayMesh *mesh, size_t index, float x, float y,
                        UiOverlayColor color) {
  if (!mesh || index >= mesh->vertex_capacity) return false;

  mesh->data[index * UI_OVERLAY_VERTEX_FLOATS + 0] = x;
  mesh->data[index * UI_OVERLAY_VERTEX_FLOATS + 1] =
      (float)mesh->viewport_h - y;
  mesh->data[index * UI_OVERLAY_VERTEX_FLOATS + 2] = color.r;
  mesh->data[index * UI_OVERLAY_VERTEX_FLOATS + 3] = color.g;
  mesh->data[index * UI_OVERLAY_VERTEX_FLOATS + 4] = color.b;
  mesh->data[index * UI_OVERLAY_VERTEX_FLOATS + 5] = color.a;
  return true;
}

static bool emit_quad(UiOverlayMesh *mesh, float x0, float y0, float x1,
                      float y1, UiOverlayColor top_left,
                      UiOverlayColor top_right, UiOverlayColor bottom_left,
                      UiOverlayColor bottom_right) {
  size_t base;

  if (!mesh || x1 <= x0 || y1 <= y0) return true;
  if (!reserve_vertices(mesh, 6)) return false;

  base = mesh->vertex_count;
  mesh->vertex_count += 6;
  return emit_vertex(mesh, base + 0, x0, y0, top_left) &&
         emit_vertex(mesh, base + 1, x1, y0, top_right) &&
         emit_vertex(mesh, base + 2, x0, y1, bottom_left) &&
         emit_vertex(mesh, base + 3, x0, y1, bottom_left) &&
         emit_vertex(mesh, base + 4, x1, y0, top_right) &&
         emit_vertex(mesh, base + 5, x1, y1, bottom_right);
}

void UiOverlay_init(UiOverlayMesh *mesh) {
  if (!mesh) return;
  memset(mesh, 0, sizeof(*mesh));
}

void UiOverlay_reset(UiOverlayMesh *mesh, int viewport_w, int viewport_h) {
  if (!mesh) return;
  mesh->vertex_count = 0;
  mesh->viewport_w = viewport_w;
  mesh->viewport_h = viewport_h;
}

void UiOverlay_free(UiOverlayMesh *mesh) {
  if (!mesh) return;
  free(mesh->data);
  mesh->data = NULL;
  mesh->vertex_count = 0;
  mesh->vertex_capacity = 0;
  mesh->viewport_w = 0;
  mesh->viewport_h = 0;
}

const float *UiOverlay_vertices(const UiOverlayMesh *mesh) {
  return mesh ? mesh->data : NULL;
}

size_t UiOverlay_vertex_count(const UiOverlayMesh *mesh) {
  return mesh ? mesh->vertex_count : 0;
}

float UiOverlay_text_height(float scale) {
  if (scale <= 0.0f) scale = 1.0f;
  return (float)UI_OVERLAY_GLYPH_H * scale;
}

float UiOverlay_text_width(const char *text, float scale, float tracking) {
  float cursor_x = 0.0f;
  float line_width = 0.0f;
  float max_width = 0.0f;
  float advance;

  if (!text) return 0.0f;
  if (scale <= 0.0f) scale = 1.0f;
  if (tracking < 0.0f) tracking = 0.0f;

  advance = (float)(UI_OVERLAY_GLYPH_W + UI_OVERLAY_GLYPH_SPACING) * scale +
            tracking;

  for (size_t i = 0; text[i] != '\0'; i++) {
    if (text[i] == '\n') {
      if (line_width > max_width) max_width = line_width;
      cursor_x = 0.0f;
      line_width = 0.0f;
      continue;
    }
    line_width = cursor_x + (float)UI_OVERLAY_GLYPH_W * scale;
    cursor_x += advance;
  }

  if (line_width > max_width) max_width = line_width;
  return max_width;
}

bool UiOverlay_add_rect(UiOverlayMesh *mesh, float x, float y, float w, float h,
                        UiOverlayColor color) {
  return emit_quad(mesh, x, y, x + w, y + h, color, color, color, color);
}

bool UiOverlay_add_hgradient(UiOverlayMesh *mesh, float x, float y, float w,
                             float h, UiOverlayColor left,
                             UiOverlayColor right) {
  return emit_quad(mesh, x, y, x + w, y + h, left, right, left, right);
}

bool UiOverlay_add_text(UiOverlayMesh *mesh, float x, float y, float scale,
                        float tracking, UiOverlayColor color,
                        const char *text) {
  float cursor_x = x;
  float cursor_y = y;
  float advance;

  if (!mesh || !text) return false;
  if (scale <= 0.0f) scale = 1.0f;
  if (tracking < 0.0f) tracking = 0.0f;

  advance = (float)(UI_OVERLAY_GLYPH_W + UI_OVERLAY_GLYPH_SPACING) * scale +
            tracking;

  for (size_t i = 0; text[i] != '\0'; i++) {
    const unsigned char *glyph;

    if (text[i] == '\n') {
      cursor_x = x;
      cursor_y += UiOverlay_text_height(scale) +
                  (float)UI_OVERLAY_LINE_SPACING * scale;
      continue;
    }

    glyph = lookup_glyph(text[i]);
    for (int row = 0; row < UI_OVERLAY_GLYPH_H; row++) {
      for (int col = 0; col < UI_OVERLAY_GLYPH_W; col++) {
        unsigned char bit =
            (unsigned char)(1u << (UI_OVERLAY_GLYPH_W - 1 - col));
        if ((glyph[row] & bit) == 0) continue;
        if (!UiOverlay_add_rect(mesh, cursor_x + (float)col * scale,
                                cursor_y + (float)row * scale, scale, scale,
                                color)) {
          return false;
        }
      }
    }
    cursor_x += advance;
  }

  return true;
}
